        // MAKS SKS
        // ============================
        function calculateMaxSks() {
            const ipVal = parseFloat(document.getElementById('mhsIp').value);
            const statusLabel = document.getElementById('ipStatusLabel');
            const sksCounter = document.getElementById('maxSksLabel');
            const progressEl = document.getElementById('sksProgress');
            if (isNaN(ipVal) || document.getElementById('mhsIp').value.trim() === '') {
                maxSksAllowed = 24; statusLabel.innerText = "Input IP Dulu"; statusLabel.className = "text-xs font-bold text-slate-500";
                progressEl.style.background = "linear-gradient(90deg, #6366f1, #818cf8)";
            } else if (ipVal < 2.00) {
                maxSksAllowed = 18; statusLabel.innerText = "Maks 18 SKS"; statusLabel.className = "text-xs font-bold text-red-400";
                progressEl.style.background = "linear-gradient(90deg, #ef4444, #f87171)";
            } else if (ipVal < 2.50) {
                maxSksAllowed = 20; statusLabel.innerText = "Maks 20 SKS"; statusLabel.className = "text-xs font-bold text-orange-400";
                progressEl.style.background = "linear-gradient(90deg, #f97316, #fb923c)";
            } else if (ipVal < 3.00) {
                maxSksAllowed = 22; statusLabel.innerText = "Maks 22 SKS"; statusLabel.className = "text-xs font-bold text-yellow-400";
                progressEl.style.background = "linear-gradient(90deg, #eab308, #facc15)";
            } else {
                maxSksAllowed = 24; statusLabel.innerText = "Maks 24 SKS"; statusLabel.className = "text-xs font-bold text-emerald-400";
                progressEl.style.background = "linear-gradient(90deg, #059669, #34d399)";
            }
            sksCounter.innerText = `/ ${maxSksAllowed} SKS`;
            calculateSks();
        }

