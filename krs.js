        function toRoman(num) {
            const map = [[10,'X'],[9,'IX'],[5,'V'],[4,'IV'],[1,'I']];
            let result = '', n = parseInt(num) || 0;
            for (const [value, symbol] of map) { while (n >= value) { result += symbol; n -= value; } }
            return result || num;
        }
        function terbilang(num) {
            const s = ['','Satu','Dua','Tiga','Empat','Lima','Enam','Tujuh','Delapan','Sembilan','Sepuluh','Sebelas','Dua Belas','Tiga Belas','Empat Belas','Lima Belas','Enam Belas','Tujuh Belas','Delapan Belas','Sembilan Belas'];
            const n = parseInt(num);
            if (n === 0) return 'Nol'; if (n < 20) return s[n];
            if (n < 100) return s[Math.floor(n / 10)] + ' Puluh' + (n % 10 ? ' ' + s[n % 10] : '');
            return num;
        }

