        function generateAcademicYears() {
            const select = document.getElementById('mhsThnAkad');
            const now = new Date();
            const currentYear = now.getFullYear();
            const currentMonth = now.getMonth();
            select.innerHTML = '';
            const isCurrentGanjil = currentMonth >= 6;
            for (let i = -1; i < 1; i++) {
                const year = currentYear + i;
                ['Ganjil', 'Genap'].forEach(term => {
                    const opt = document.createElement('option');
                    opt.value = `${year}/${year + 1} ${term}`;
                    opt.textContent = `${year}/${year + 1} ${term}`;
                    if (i === 0 && isCurrentGanjil === (term === 'Ganjil')) opt.selected = true;
                    select.appendChild(opt);
                });
            }
        }

