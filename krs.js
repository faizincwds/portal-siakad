        // VALIDASI
        // ============================
        function showError(fieldId, msg) {
            document.getElementById(fieldId).classList.add('field-error');
            const errEl = document.getElementById('err-' + fieldId);
            if (errEl) { errEl.textContent = msg; errEl.classList.add('visible'); }
        }
        function clearError(fieldId) {
            document.getElementById(fieldId).classList.remove('field-error');
            const errEl = document.getElementById('err-' + fieldId);
            if (errEl) errEl.classList.remove('visible');
        }
        function clearAllErrors() {
            document.querySelectorAll('.field-error').forEach(el => el.classList.remove('field-error'));
            document.querySelectorAll('.error-msg').forEach(el => el.classList.remove('visible'));
        }
        function validateForm() {
            clearAllErrors();
            let valid = true;
            if (!document.getElementById('mhsNama').value.trim()) { showError('mhsNama', 'Nama mahasiswa wajib diisi'); valid = false; }
            if (!document.getElementById('mhsNim').value.trim()) { showError('mhsNim', 'NIM wajib diisi'); valid = false; }
            if (!document.getElementById('mhsIp').value.trim()) { showError('mhsIp', 'IP Semester Lalu wajib diisi'); valid = false; }
            if (!document.getElementById('mhsAngkatan').value.trim()) { showError('mhsAngkatan', 'Angkatan wajib diisi'); valid = false; }
            if (!document.getElementById('semester').value.trim()) { showError('semester', 'Semester wajib diisi'); valid = false; }
            if (!selectedMK.length) { showToast('Pilih minimal satu mata kuliah', 'warning'); valid = false; }
            return valid;
        }

